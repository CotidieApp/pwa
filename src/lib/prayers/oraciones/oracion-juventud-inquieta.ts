  title: 'Oración de la juventud inquieta',
  content: `¡Qué bien se está contigo, Señor, aunque no lo merezca!
Qué bien se está contigo, aunque me duela el corazón.
Aquí, tan cerca de Ti, me tiemblan los sueños,
y aún así no quiero irme, aunque no entienda nada.

Tú lo sabes todo, Señor mío:
sabes que estoy asustado,
que me cuesta dejar mi casa,
que me asusta equivocarme,
que me duele tan solo pensarlo.

Sabes que no es falta de amor,
sino debilidad humana,
una que Tú conoces mejor que nadie.
Sabes que quiero lo que Tú quieres,
pero que me falta valentía para decirte que sí sin condiciones.

¿Por qué no me hablas más claro?
¿No ves que me muero por seguirte
pero no sé a dónde ir?
¿No ves que quiero dejarlo todo
pero me cuesta dejar incluso mi pieza?
¿Por qué no me hablas más fuerte, Señor?

Y sin embargo, ahí estás:
firme, silencioso, atento,
más paciente que todos los hombres juntos.
Ahí estás, mirándome sin presionarme,
esperando que me atreva a dar el paso,
ese paso que no me exiges, pero que deseas con ternura.

Tú lo podrías todo, y aún así esperas por mí.
Yo, que no soy nada,
y Tú, que lo eres todo,
y aún así me llamas con la voz más mansa del universo.

¿Por qué no me atrevo, Señor,
si sé que lo tuyo es lo mejor?
¿Por qué sigo dudando,
si cada vez que te elijo, encuentro la paz?
¿Por qué tengo miedo,
si nunca me has dejado solo?

Si quieres que te siga, Señor,
dímelo, o hazlo arder más fuerte aún.
Pero no me dejes aquí en esta tibieza que me consume.

Quita de mí el miedo, la flojera, el afán de controlarlo todo.
Quita de mí el amor a la comodidad,
y enciende en mí ese Amor que no busca consuelo,
sino que consuela.

Jesús mío,
si me quieres todo tuyo,
si me quieres en ese lugar,
no me sueltes más.
Empujame si hace falta,
pero no me dejes vivir lejos de tu Voluntad.

¿Dónde podría estar mejor que a tu lado?
¿Dónde podría amar más que contigo?
¿Dónde podría vivir más plenamente que en tus caminos?

Si sabes todo esto,
¿por qué no me lanzo ya?
Si ya me esperas,
¿por qué no corro hasta Ti?

Señor,
ya no te pido pruebas ni voces fuertes.
Solo te pido que no me dejes en el miedo,
que me abraces en el silencio,
y me sostengas mientras avanzo.

¡Qué bien se está contigo, Señor!
Aunque tenga miedo. Aunque no entienda.
¡Qué bien se está contigo!
Y ojalá, algún día,
pueda decirte con todo el corazón:
¡Aquí estoy, Señor, para hacer Tu Voluntad!

Amén.`
